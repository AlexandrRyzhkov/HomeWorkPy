# Петя, Сережа и Катя
s = int(input("Сколько журавликов ?\n"))
p = s // 2 // 2 #1/4 у Пети
c = s // 2 // 2 #1/4 у Сережи
k = s // 2 #1/2 у Кати
x = p + c + k
if x == s:
    print("Петя сделал:",p,"\nСережа сделал:",c,"\nКатя сделала:",k)
else:
    y = s % x
    print("Петя сделал:",p,"\nСережа сделал:",c,"\nКатя сделала:",k,"\nКатя принесла с собой:",y)